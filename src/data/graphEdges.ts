import type { KnowledgeEdge } from "../types/knowledge";
import { learningPaths } from "./learningPaths";
import { projectEvidence } from "./projects";
import { topics } from "./topics";

type EdgeSeed = Omit<KnowledgeEdge, "id">;

const topicTitleById = new Map(topics.map((topic) => [topic.id, topic.title]));
const projectTitleById = new Map(
  projectEvidence.map((item) => [item.id, item.title]),
);
const titleById = new Map([...topicTitleById, ...projectTitleById]);

const titleOf = (id: string) => titleById.get(id) ?? id;

const edgeId = (edge: EdgeSeed) =>
  `edge-${edge.kind}-${edge.from}-to-${edge.to}`;

const createEdge = (edge: EdgeSeed): KnowledgeEdge => ({
  id: edgeId(edge),
  ...edge,
});

const prerequisiteEdges: KnowledgeEdge[] = topics.flatMap((topic) =>
  topic.prerequisites.map((prerequisiteId) =>
    createEdge({
      from: prerequisiteId,
      to: topic.id,
      kind: "prerequisite",
      label: "前置",
      strength: topic.priority === "extension" ? 2 : 3,
      reason: `${titleOf(prerequisiteId)} 是理解「${topic.title}」的前置知识。`,
    }),
  ),
);

const samePathEdges: KnowledgeEdge[] = learningPaths.flatMap((path) =>
  path.nodeIds.slice(0, -1).map((nodeId, index) =>
    createEdge({
      from: nodeId,
      to: path.nodeIds[index + 1],
      kind: "same_path",
      label: "路径推进",
      strength: 2,
      reason: `在「${path.title}」中，先掌握「${titleOf(nodeId)}」再推进到「${titleOf(
        path.nodeIds[index + 1],
      )}」。`,
      pathIds: [path.id],
    }),
  ),
);

const projectEvidenceEdges: KnowledgeEdge[] = projectEvidence.flatMap((item) =>
  item.relatedTopicIds.map((topicId) =>
    createEdge({
      from: topicId,
      to: item.id,
      kind: "project_evidence",
      label: "项目证据",
      strength: 3,
      reason: `「${titleOf(topicId)}」可以落到「${item.title}」这条项目表达线上。`,
    }),
  ),
);

